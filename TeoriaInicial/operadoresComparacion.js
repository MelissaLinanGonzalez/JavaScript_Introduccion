// OPeradores Relacionales (comparación)

let a = 5;
let b = '5';

console.log(a);
console.log(b);

// Operadores de igualdad ==
// Solo compara valores y hace una conversión de tipos si es necesario
console.log("a == b ->", a == b)

// Operador de igualdad estricta ===
// Compara valor y tipo de dato
console.log("a === b ->", a === b)

// String interpolation
console.log(`${a} === ${b} -> ${a === b}`);


// Operdadores de desigualdad
console.log(`${a} != ${b} -> ${a != b}`);

// Operador distinto exacto
console.log(`${a} !== ${b} -> ${a !== b}`);

// Operador manor que
console.log(`${a} < ${b} -> ${a < b}`);

// Operador menor o igual que
console.log(`${a} <= ${b} -> ${a <= b}`);

// Operador mayor que
console.log(`${a} > ${b} -> ${a > b}`);

// Operador mayor o igual que
console.log(`${a} >= ${b} -> ${a >= b}`);