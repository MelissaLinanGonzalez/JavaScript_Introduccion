// Operadores Lógicos
let a = true;
let b = false;

console.log(a);
console.log(b);

// Operador && (AND)
// Regresa TRUE si ambos operandos son TRUE
console.log(`${a} && ${b} -> ${a && b}`);

// Operador || (OR)
// Regresa TRUE si al menos uno de los operadores es TRUE
console.log(`${a} || ${b} -> ${a || b}`);

// Operador ! (NOT)
// Invierte el valor lógico del operador
console.log(`${a} -> ${!a}`);
console.log(`${b} -> ${!b}`);